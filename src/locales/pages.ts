export const getPageText = (countryId: string, page: string, key: string) => {
  const dict = pageTranslations[countryId] || pageTranslations['SE'];
  const pageDict = dict[page] || pageTranslations['UK'][page];
  return pageDict[key] || pageTranslations['UK'][page][key] || key;
};
